package edu.fjnu.xtw.domain;

import java.util.Arrays;

/**
 * @author lzx
 * 
 */
public class XtwGoods {
	private Integer goodsId;
	private XtwType xtwType;
	private byte[] picture;
	private String brand;
	private String cardNumber;
	private String ownerName;

	public XtwGoods() {
		super();
	}

	public XtwGoods(Integer goodsId, XtwType xtwType, byte[] picture,
			String brand, String cardNumber, String ownerName) {
		super();
		this.goodsId = goodsId;
		this.xtwType = xtwType;
		this.picture = picture;
		this.brand = brand;
		this.cardNumber = cardNumber;
		this.ownerName = ownerName;
	}

	public Integer getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}

	public XtwType getXtwType() {
		return xtwType;
	}

	public void setXtwType(XtwType xtwType) {
		this.xtwType = xtwType;
	}

	public byte[] getPicture() {
		return picture;
	}

	public void setPicture(byte[] picture) {
		this.picture = picture;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getCardNumber() {
		return cardNumber;
	}

	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}

	public String getOwnerName() {
		return ownerName;
	}

	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	@Override
	public String toString() {
		return "XtwGoods [goodsId=" + goodsId + ", xtwType=" + xtwType
				+ ", picture=" + Arrays.toString(picture) + ", brand=" + brand
				+ ", cardNumber=" + cardNumber + ", ownerName=" + ownerName
				+ "]";
	}

}
